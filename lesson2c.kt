fun main() {
    // if ...else if ... else conditional statement
    // This is used to evaluate multuple conditions . The else part get executed when all the conditions given evaluate to false

    val number = 0

    if (number > 0){
        println("The number is positive")
    }
    else if (number == 0){
        println(" The numberr is Zero")
    }
    else{
        println("The number is negative")
    }
}